import { readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '../../..');
const metadataPath = resolve(
  repoRoot,
  'node_modules/@fortawesome/fontawesome-free/metadata/icon-families.json'
);
const outputPath = resolve(__dirname, '../src/types/fontawesome.ts');

const styleToPrefix = new Map([
  ['solid', 'fa-solid'],
  ['regular', 'fa-regular'],
  ['brands', 'fa-brands'],
]);

const raw = await readFile(metadataPath, 'utf8');
const data = JSON.parse(raw);

const iconNames = new Set();
const prefixes = new Set();
const classNames = new Set();

for (const [name, info] of Object.entries(data)) {
  const families = Array.isArray(info?.familyStylesByLicense?.free)
    ? info.familyStylesByLicense.free
    : [];
  const styles = families
    .filter((entry) => entry.family === 'classic')
    .map((entry) => entry.style);
  const hasSupportedStyle = styles.some((style) => styleToPrefix.has(style));
  if (!hasSupportedStyle) continue;
  iconNames.add(name);
  for (const style of styles) {
    const prefix = styleToPrefix.get(style);
    if (!prefix) continue;
    prefixes.add(prefix);
    classNames.add(`${prefix} fa-${name}`);
  }
}

const sortedIcons = [...iconNames].sort();
const sortedPrefixes = [...prefixes].sort();
const sortedClassNames = [...classNames].sort();

const file = [
  '// This file is auto-generated by scripts/generate-fa-types.mjs',
  '// Source: @fortawesome/fontawesome-free/metadata/icon-families.json',
  '// Do not edit by hand.',
  '',
  `export const faPrefixes = [${sortedPrefixes.map((p) => `'${p}'`).join(', ')}] as const;`,
  `export type FaPrefix = typeof faPrefixes[number];`,
  '',
  `export const faIconNames = [${sortedIcons.map((n) => `'${n}'`).join(', ')}] as const;`,
  `export type FaIconName = typeof faIconNames[number];`,
  '',
  `export const faClassNames = [${sortedClassNames.map((n) => `'${n}'`).join(', ')}] as const;`,
  `export type FaClassName = typeof faClassNames[number];`,
  '',
].join('\n');

await writeFile(outputPath, file, 'utf8');

console.log(`Generated ${outputPath} with ${sortedIcons.length} icons.`);
